    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String roll = (String)jTextField7.getText();
        String physics = (String)jTextField8.getText();
        String chemistry = (String)jTextField9.getText();
        String maths = (String)jTextField10.getText();
        String cs = (String)jTextField11.getText();
        String ethics = (String)jTextField12.getText();

        try{

            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Jaisairam123!");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from students where roll='"+roll+"'");
            
            if(rs.next()){
                st.executeUpdate("insert into results values('"+roll+"','"+physics+"','"+chemistry+"','"+maths+"','"+cs+"','"+ethics+"')");
                JOptionPane.showMessageDialog(null, "Successfully Added Marks Of The Student");
                setVisible(false);
                new InsertNewResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student with this Roll Number does not exist . Please check and enter the correct roll number");
            }
        }

        catch(Exception error){
            JOptionPane.showMessageDialog(null, error.toString());

        }

    }//GEN-LAST:event_jButton7ActionPerformed

