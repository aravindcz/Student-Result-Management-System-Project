    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try{

            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Jaisairam123!");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from students inner join results where students.roll='"+roll+"' and results.roll='"+roll+"'");
            
            if(rs.next()){
            
                jTextField4.setText(String.valueOf(roll));
                jTextField2.setText(String.valueOf(rs.getString(4)));
                jTextField3.setText(String.valueOf(rs.getString(6)));
                jTextField1.setText(String.valueOf(rs.getString(1)));
                jTextField6.setText(String.valueOf(rs.getString(2)));
                jTextField5.setText(String.valueOf(rs.getString(5)));
                
                jTextField20.setText(String.valueOf(rs.getString(8)));
                jTextField21.setText(String.valueOf(rs.getString(9)));
                jTextField22.setText(String.valueOf(rs.getString(10)));
                jTextField23.setText(String.valueOf(rs.getString(11)));
                jTextField24.setText(String.valueOf(rs.getString(12)));
                
                int totalMarks=0;
                
                int physics = Integer.parseInt(jTextField20.getText());
                int chemistry = Integer.parseInt(jTextField21.getText());
                int maths = Integer.parseInt(jTextField22.getText());
                int cs = Integer.parseInt(jTextField23.getText());
                int ethics = Integer.parseInt(jTextField24.getText());
                
                totalMarks = physics + chemistry + maths + cs + ethics;
                
                jTextField25.setText(String.valueOf(totalMarks));
                
                if(physics<30 || chemistry<30 || maths<30 || cs<30 || ethics<30)
                    jTextField14.setText("FAIL");
                else
                    jTextField14.setText("PASS");
        
                
            }
            
        }

        catch(Exception error){
            JOptionPane.showMessageDialog(null, "Connection Issue");
    }                       
    }//GEN-LAST:event_formComponentShown

